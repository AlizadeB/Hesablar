package com.company;

public class Emeliyyatlar {

    public static Integer toplama(int a, int b) {
        return a + b;
    }

    public static Integer cixma(int a, int b) {
        return a - b;
    }

    public static Integer vurma(int a, int b) {
        return a * b;
    }

    public static Integer bolme(int a, int b) {
        return a / b;
    }
}
